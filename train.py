from ultralytics import YOLO

# ============================================================================
# 1. 模型初始化
# ============================================================================

# 方案 A：从配置文件创建全新的 YOLO11n 模型（从头训练）
# yolo11n.yaml 定义了最轻量级的 YOLO11 网络结构
model = YOLO("yolo11n.yaml")

# 方案 B：加载预训练权重（推荐，收敛更快）
# 取消下面这行的注释来使用预训练模型
# model = YOLO("yolo11n.pt")

# ============================================================================
# 2. 模型训练配置
# ============================================================================

# 使用 COCO8 数据集（COCO train2017 的前 8 张图像，用于快速验证）
# amp=False: 关闭自动混合精度训练，确保数值稳定性
# epochs=100: 训练 100 个轮次
# imgsz=640: 输入图像尺寸 640x640
# device=5: 使用 CUDA device 5（根据实际 GPU 环境调整）
# pretrained=False: 不加载预训练权重，从头开始训练
results = model.train(
    amp=False,                    # 关闭自动混合精度
    data="coco8.yaml",            # 数据集配置文件
    epochs=100,                   # 训练轮次
    imgsz=640,                    # 输入图像尺寸
    device=5,                     # GPU 设备编号
    pretrained=False              # 从头训练，不使用预训练权重
)

# ============================================================================
# 3. 模型验证
# ============================================================================

# 在验证集上评估模型性能
# 输出指标包括：mAP50, mAP50-95, precision, recall 等
results = model.val()

# ============================================================================
# 4. 目标检测推理
# ============================================================================

# 对单张图像进行目标检测
# 模型将输出检测框、类别置信度等信息
results = model("https://ultralytics.com/images/bus.jpg")

# ============================================================================
# 5. 模型导出
# ============================================================================

# 将训练好的模型导出为 ONNX 格式
# ONNX 格式可用于跨平台部署（如 TensorRT, OpenVINO 等）
success = model.export(format="onnx")