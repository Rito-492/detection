from ultralytics import YOLO

# ============================================================================
# 1. 设备配置
# ============================================================================

# 指定 GPU 设备
# 'cuda:2' 表示使用第 2 号 GPU 设备（从 0 开始计数）
# 如果只有一块 GPU，可以改为 'cuda:0' 或 'cuda'
# 如果使用 CPU，改为 'cpu'（不推荐，速度很慢）
device = 'cuda:2'

# ============================================================================
# 2. 加载模型
# ============================================================================

# 加载训练好的 YOLO11n 模型
# 路径 "runs/detect/train3/weights/yolo11n.pt" 是训练后保存的权重文件
# YOLO11n 是 YOLO11 系列中最轻量级的模型，适合快速推理
model = YOLO("runs/detect/train3/weights/yolo11n.pt")

# ============================================================================
# 3. 执行目标检测
# ============================================================================

# 对图像进行目标检测
# 模型会自动识别图像中的物体，并输出：
# - 检测框坐标 (boxes.xyxy)
# - 类别置信度 (boxes.conf)
# - 类别标签 (boxes.cls)
results = model("YOLOO/bus.jpg", device=device)

# ============================================================================
# 4. 处理检测结果
# ============================================================================

for result in results:
    # 打印检测框信息
    # boxes 属性包含：坐标、置信度、类别
    print(result.boxes)

    # 保存检测结果图像
    # 结果图像会包含检测框和类别标签
    result.save()